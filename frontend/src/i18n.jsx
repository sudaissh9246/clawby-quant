const I18nCtx = React.createContext({ lang: 'zh', t: (k) => k, pick: (o) => o, setLang: () => {} })

export function I18nProvider({ children }) {
  const [lang, setLangState] = React.useState(() => {
    const q = new URLSearchParams(window.location.search).get('lang')
    return (q === 'en' || q === 'zh') ? q : (localStorage.getItem('qb_lang') || 'zh')
  })
  const setLang = (l) => { localStorage.setItem('qb_lang', l); setLangState(l) }
  const t = React.useCallback(
    (key) => DICT[lang][key] ?? DICT.zh[key] ?? key, [lang])
  // pick bilingual content data: pick(meta, 'logic') -> logic_en when lang=en
  const pick = React.useCallback((obj, field) => {
    if (!obj) return ''
    if (lang === 'en') return obj[`${field}_en`] || obj[field] || ''
    return obj[field] || ''
  }, [lang])
  const value = React.useMemo(() => ({ lang, t, pick, setLang }), [lang, t, pick])
  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>
}

export const useI18n = () => React.useContext(I18nCtx)
